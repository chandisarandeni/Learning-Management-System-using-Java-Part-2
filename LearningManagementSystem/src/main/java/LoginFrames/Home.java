    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling code here:
        String studentID = StudentIDRetriever.getStudentIDByEmail(txt_studentUsername.getText().toLowerCase());

        if (studentID != null) {
            System.out.println("Student ID: " + studentID);
        } else {
            System.out.println("No student found with this email.");
        }

        String Username = txt_studentUsername.getText();

        if (Username.equals("./admin")) {
            AdminLogin adminLogin = new AdminLogin();
            adminLogin.setVisible(true);
            this.setVisible(false);
        } else if (Username.equals("./lec")) {
            LecturerLogin lecturerLogin = new LecturerLogin();
            lecturerLogin.setVisible(true);
            this.setVisible(false);
        } else {
            // Database connection details
            String connectionString = "jdbc:mysql://localhost:3306/LMS"; // Update with your DB details
            String dbUsername = "root";
            String dbPassword = "";

            Connection conn = null;
            PreparedStatement stmt = null;
            ResultSet rs = null;

            try {
                // Get user input
                String studentEmail = txt_studentUsername.getText();
                String studentPassword = new String(txt_studentPassword.getPassword());

                // Connect to the database
                conn = DriverManager.getConnection(connectionString, dbUsername, dbPassword);

                // SQL query to validate login
                String sql = "SELECT studentEmail, studentPassword FROM Student WHERE studentEmail = ? AND studentPassword = ?";
                stmt = conn.prepareStatement(sql);
                stmt.setString(1, studentEmail);  // Corrected from 'txt_studentUsername' to 'studentEmail'
                stmt.setString(2, studentPassword);

                // Execute query
                rs = stmt.executeQuery();

                if (rs.next()) {
                    // Login successful
                    StudentDashboard studentDashboard = new StudentDashboard(studentID);
                    studentDashboard.setVisible(true);
                    this.setVisible(false);
                } else {
                    // Invalid login
                    JOptionPane.showMessageDialog(this, "Invalid email or password");
                    txt_studentUsername.setText("");
                    txt_studentPassword.setText("");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
                txt_studentUsername.setText("");
                txt_studentPassword.setText("");
            } finally {
                // Close resources
                try {
                    if (rs != null) {
                        rs.close();
                    }
                    if (stmt != null) {
                        stmt.close();
                    }
                    if (conn != null) {
                        conn.close();
                    }
                } catch (SQLException ex) {
                    txt_studentUsername.setText("");
                    txt_studentPassword.setText("");
                    JOptionPane.showMessageDialog(this, "Error Closing Resources: " + ex.getMessage());
                }
            }
        }


    }//GEN-LAST:event_btn_LoginActionPerformed

